# ── 5. Monkey-patch ───────────────────────────────────────────────────────────
cells.append(md_cell("## Section 4 \u00b7 Monkey-Patch Infrastructure"))

cells.append(code_cell(
    "# --- BboxLoss monkey-patch (same pattern as notebook 03)\n"
    "# Replaces the IoU term in Ultralytics BboxLoss.forward with any custom loss.\n"
    "# Works for YOLOv26s and RT-DETR-L (both use BboxLoss internally).\n"
    "import types, torch, torch.nn.functional as F\n"
    "import ultralytics.utils.loss as loss_mod\n\n"
    "_ORIGINAL_BBOX_FORWARD = loss_mod.BboxLoss.forward\n\n"
    "def _make_bbox_forward(loss_fn):\n"
    "    def bbox_loss_forward(\n"
    "        self, pred_dist, pred_bboxes, anchor_points,\n"
    "        target_bboxes, target_scores, target_scores_sum, fg_mask, imgsz, stride,\n"
    "    ):\n"
    "        weight = target_scores.sum(-1)[fg_mask].unsqueeze(-1)\n"
    "        per_box = loss_fn(pred_bboxes[fg_mask], target_bboxes[fg_mask])\n"
    "        loss_iou = (per_box.unsqueeze(-1) * weight).sum() / target_scores_sum\n"
    "        if self.dfl_loss:\n"
    "            target_ltrb = loss_mod.bbox2dist(\n"
    "                anchor_points, target_bboxes, self.dfl_loss.reg_max - 1)\n"
    "            loss_dfl = self.dfl_loss(\n"
    "                pred_dist[fg_mask].view(-1, self.dfl_loss.reg_max),\n"
    "                target_ltrb[fg_mask],\n"
    "            ) * weight\n"
    "            loss_dfl = loss_dfl.sum() / target_scores_sum\n"
    "        else:\n"
    "            target_ltrb = loss_mod.bbox2dist(anchor_points, target_bboxes)\n"
    "            target_ltrb = target_ltrb * stride\n"
    "            target_ltrb[..., 0::2] /= imgsz[1]; target_ltrb[..., 1::2] /= imgsz[0]\n"
    "            pred_dist_s = pred_dist * stride\n"
    "            pred_dist_s[..., 0::2] /= imgsz[1]; pred_dist_s[..., 1::2] /= imgsz[0]\n"
    "            loss_dfl = (\n"
    "                F.l1_loss(pred_dist_s[fg_mask], target_ltrb[fg_mask],\n"
    "                          reduction='none').mean(-1, keepdim=True) * weight\n"
    "            ).sum() / target_scores_sum\n"
    "        return loss_iou, loss_dfl\n"
    "    return bbox_loss_forward\n\n"
    "def patch_loss(loss_fn):\n"
    "    loss_mod.BboxLoss.forward = _make_bbox_forward(loss_fn)\n"
    "    tag = f'(lambda={loss_fn.rigidity})' if hasattr(loss_fn, 'rigidity') else ''\n"
    "    print(f'  [PATCH] BboxLoss -> {type(loss_fn).__name__}{tag}')\n\n"
    "def restore_loss():\n"
    "    loss_mod.BboxLoss.forward = _ORIGINAL_BBOX_FORWARD\n\n"
    "print('Monkey-patch infrastructure ready.')\n"
))

