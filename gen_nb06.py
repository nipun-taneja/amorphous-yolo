    "# --- Register AEIoULossMMDet in MMDetection loss registry\n"
    "# Thin wrapper that calls our existing AEIoULoss with the MMDet calling convention.\n"
    "import torch, torch.nn as nn\n"
    "from mmdet.registry import MODELS\n"
    "from src.losses import AEIoULoss as _AEIoU, EIoULoss as _EIoU, CIoULoss as _CIoU, ECIoULoss as _ECIoU\n\n"
    "def _make_mmdet_loss(loss_cls, **kwargs):\n"
    "    \"\"\"Factory: wraps a src/losses.py class for MMDetection's loss API.\"\"\"\n"
    "    _reduction = kwargs.pop('reduction', 'none')\n"
    "    _loss_weight = kwargs.pop('loss_weight', 1.0)\n"
    "    _inner = loss_cls(reduction='none', **kwargs)\n\n"
    "    @MODELS.register_module(name=f'{loss_cls.__name__}MMDet', force=True)\n"
    "    class _Wrapped(nn.Module):\n"
    "        def __init__(self): super().__init__()\n"
    "        def forward(self, pred, target, weight=None, avg_factor=None, **kw):\n"
    "            loss = _inner(pred, target)\n"
    "            if weight is not None: loss = loss * weight\n"
    "            if avg_factor is not None: return loss.sum() / avg_factor\n"
    "            return loss.mean()\n"
